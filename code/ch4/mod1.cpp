#include <iostream>

using std::cin;
using std::cout;
using std::endl;

int main() {
    cout << "21 % 6 = " << 21 % 6 << endl; // 3
    cout << "21 / 6 = " << 21 / 6 << endl; // 3

    cout << "21 % 7 = " << 21 % 7 << endl; // 0
    cout << "21 / 7 = " << 21 / 7 << endl; // 3

    cout << "-21 % -8 = " << -21 % -8 << endl; // -5
    cout << "-21 / -8 = " << -21 / -8 << endl; // 2

    cout << "21 % -5 = " << 21 % -5 << endl; // 1
    cout << "21 / -5 = " << 21 / -5 << endl; // -4
}